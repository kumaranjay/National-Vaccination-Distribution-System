    public void populate() {
        DefaultTableModel dtm = (DefaultTableModel) vaccineJTable.getModel();
        dtm.setRowCount(0);
        for (Vaccine vaccine : system.getVaccineDirectory().getVaccineList()) {

            Object row[] = new Object[2];
            row[0] = vaccine.getVaccineId();
            row[1] = vaccine;
            dtm.addRow(row);
        }

    }

