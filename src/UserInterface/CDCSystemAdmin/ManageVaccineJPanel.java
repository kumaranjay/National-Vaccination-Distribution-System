    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = vaccineJTable.getSelectedRow();
        if (selectedRow >= 0) {
            Vaccine vaccine = (Vaccine) vaccineJTable.getValueAt(selectedRow, 1);
            ViewVaccineCDCJPanel viewVaccineJPanel = new ViewVaccineCDCJPanel(userProcessContainer, system, new ManageVaccineJPanel(userProcessContainer, system), vaccine);
            userProcessContainer.add("ViewVaccineJPanel", viewVaccineJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row");
        }
    }//GEN-LAST:event_editButtonActionPerformed

