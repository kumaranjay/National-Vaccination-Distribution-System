    private void approveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_approveButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = requestJTable.getSelectedRow();
        if (selectedRow >= 0) {
            OrderRequest orderRequest = new OrderRequest();
            orderRequest = (OrderRequest) requestJTable.getValueAt(selectedRow, 0);
            if(orderRequest.getStatus().equals("Approved by PHD & Pending from CDC")){
                JOptionPane.showMessageDialog(null, "Order already processed");
                return;
            }
            if(orderRequest.getStatus().equals("Rejected by PHD")){
                JOptionPane.showMessageDialog(null, "Order already processed");
                return;
            }
            orderRequest.setStatus("Approved by PHD & Pending from CDC");
            Date date = new Date();
            orderRequest.setResolveDate(date);
            WorkRequest request = orderRequest;
            system.getWorkQueue().getWorkRequestList().add(request);
            JOptionPane.showMessageDialog(null, "Request forwarded to CDC");
            populateRequests();
        } else if (selectedRow > 1) {
            JOptionPane.showMessageDialog(null, "Please select only one row");
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row");
        }
    }//GEN-LAST:event_approveButtonActionPerformed

