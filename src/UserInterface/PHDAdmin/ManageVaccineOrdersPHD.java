    public void populateRequests() {
        DefaultTableModel dtm = (DefaultTableModel) requestJTable.getModel();
        dtm.setRowCount(0);
        String registered;

        for (WorkRequest request : account.getWorkQueue().getWorkRequestList()) {
            if(request.getRequestType()=="Order"){
            OrderRequest orderRequest = (OrderRequest) request;
            Object row[] = new Object[6];
            row[0] = orderRequest;
            row[1] = orderRequest.getStatus();
            row[2] = orderRequest.getRequestDate();
            row[3] = orderRequest.getSenderEnterprise().getEnterpriseName();
            row[4] = orderRequest.getSender();
            ProviderEnterprise provider = (ProviderEnterprise) orderRequest.getSenderEnterprise();
            if (provider.getStatus().equals("Active")) {
                registered = "yes";
            } else {
                registered = "no";
            }
            row[5] = registered;
            dtm.addRow(row);
        }}
    }

    public void populateOrder(Order order) {
        DefaultTableModel dtm = (DefaultTableModel) orderTable.getModel();
        dtm.setRowCount(0);
        for (OrderItem orderItem : order.getOrderItemList()) {
            Object row[] = new Object[2];
            row[0] = orderItem.getVaccine().getVaccineName();
            row[1] = orderItem.getQuantity();
            dtm.addRow(row);
        }
    }

