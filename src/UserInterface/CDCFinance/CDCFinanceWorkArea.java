    private void payAmountButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_payAmountButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = paymentTable.getSelectedRow();
        FinanceRequest selectedOrder = null;
        if (selectedRow >= 0) {
            int requestID = (Integer) paymentTable.getValueAt(selectedRow, 0);
            for (WorkRequest request : system.getWorkQueue().getWorkRequestList()) {
                if (request.getRequestID() == requestID) {
                    selectedOrder = (FinanceRequest) request;
                    break;
                }
            }
            Date date = new Date();
            selectedOrder.setPaidDate(date);
            selectedOrder.setStatus("Paid by CDC");
            JOptionPane.showMessageDialog(null, "Payment Paid Successfully");
        } else if (selectedRow > 1) {
            JOptionPane.showMessageDialog(null, "Please select only one row");
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row");
        }
        populate();
        totalAmountTextField.setText(null);
        billingDateTextField.setText(null);

    }//GEN-LAST:event_payAmountButtonActionPerformed

