    private void viewOrderButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewOrderButton2ActionPerformed
        // TODO add your handling code here:
        int selectedRow = paymentTable.getSelectedRow();
        FinanceRequest selectedOrder = null;
        if (selectedRow >= 0) {
            int requestID = (Integer) paymentTable.getValueAt(selectedRow, 0);
            for (WorkRequest request : system.getWorkQueue().getWorkRequestList()) {
                if (request.getRequestID() == requestID) {
                    selectedOrder = (FinanceRequest) request;
                }
            }
            totalAmountTextField.setText(String.valueOf(selectedOrder.getTotalAmount()));
            billingDateTextField.setText(String.valueOf(selectedOrder.getBillingDate()));
            firstNameTextField.setText(selectedOrder.getPatient().getPatientName());
            lastNameTextField.setText(selectedOrder.getPatient().getLastName());
            ageTextField.setText(String.valueOf(selectedOrder.getPatient().getAge()));
            contactNumberTextField.setText(selectedOrder.getPatient().getContactNo());
            insuranceNameTextField.setText(selectedOrder.getPatient().getInsuranceName());
            insurancePercentageTextField.setText(String.valueOf(selectedOrder.getPatient().getInsurancePercentage()));
            insuranceTypeTextField.setText(selectedOrder.getPatient().getInsuredType());

        } else if (selectedRow > 1) {
            JOptionPane.showMessageDialog(null, "Please select only one row");
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row");
        }
    }//GEN-LAST:event_viewOrderButton2ActionPerformed

